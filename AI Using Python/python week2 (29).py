# core concept of opp----classes,objects,mtethods and attributes
# classes----Asan alfaz mein samjhein toh OOP aik aisa tareeka hai jis mein hum
# real-world cheezon ko code mein dhaal dete hain. Is ke 4 main hissay yeh hain:
#
#    1. Class (Blueprint/Naqsha):
#    Yeh aik "naqsha" ya "sancha" hota hai. Jaise aik ghar bananay se pehle uska
#    kaghaz par naqsha banta hai. Naqsha khud ghar nahi hota, bas batata hai ke ghar
#    kaisa hoga.
#
#
# * Misaal: "Car" ka aik concept ya naqsha.
#
#
#    1. Object (Asal Cheez/Instance):
#    Jab aap us naqshay ko dekh kar asal mein ghar khara kar dete hain, toh wo
#    "Object" hai. Aik hi naqshay se 10 alag ghar ban saktay hain.
#
#
# * Misaal: Aap ki apni "Civic" ya "Corolla" car jo sarak par khari hai.
#
#
#    1. Attributes (Khoobiyan/Data):
#    Yeh batatay hain ke object kaisa dikhta hai ya uski properties kya hain.
#    Isay hum "variables" bhi kehte hain.
#
#
# * Misaal: Car ka rang (red), model (2024), ya engine power (1300cc).
#
#
#    1. Methods (Kaam/Behavior):
#    Yeh wo "functions" hain jo batatay hain ke object kya kar sakta hai.
#
#
# * Misaal: Car ka chalna (drive), break lagana (brake), ya horn bajana.
#
# ------------------------------
# ## Aik Saadi Misaal (Mobile Phone):
#
# * Class: Mobile Phone ka design (ke har mobile mein screen hogi aur battery hogi).
# * Object: Aapka apna Samsung ya iPhone jo aapke hath mein hai.
# * Attributes: Screen size, RAM, color.
# * Methods: Call karna, Message bhejna, Game khelna.



