# introduction to numpy,pandas and matplotlib
# pandas and numpy-->use for mostly to store and manipulate data



# numpy:is  mostly used for arrays
# library for numerical computations
# supports multidimentional arrays and matrices
# used for large calculation
# enables efficient mathematical operations(linea algebra,statistics)
# data manipulation--->the process of organizing, modifying, and transforming
# raw information
# to make it cleaner, more readable, and easier to analyze


# pandas:
# is used for matrices or multidimentional arrays
# library for data analysis and manipulation
# provide dataframe and series structure
# simplifies working with structured data(csv,excel,sql)



# matplotlib______
# widely used for data visualization library(Data visualization is the graphical
# representation of information and data, using visual elements like charts, graphs,
# and maps to make complex datasets easy to understand, analyze, and communicate)
# creates charts:line,bar,scatter, histograms
# low level interface,requires more code
# generals purpose library support wide range of plot (line,bar,scatter,pie ,3d etc
#  overall means its best for simple data maniulation



# seaborn-->built on top of matplotlib
# provides high level,visually appealing plots
# idea for statistical data--->Wo numbered information jo hum measure kar sakein, calculate
# kar sakein aur us se meaningful conclusions nikaal sakein.
# high level interface,simpler syntax
# specialize in statistical visualization(heatmaps,violin plots,pair plots,regressio plots)
# overall means its best for statistical data


